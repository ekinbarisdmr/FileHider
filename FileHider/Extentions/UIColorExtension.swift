import Foundation
import UIKit

extension UIColor {
    
    class func jaguarColor() -> UIColor {
        
        return UIColor(red: 41/255, green: 42/255, blue: 49/255, alpha: 1.0)
    }
    
    class func selectiveYellow() -> UIColor {
        
        return UIColor(red: 255/255, green: 183/255, blue: 0/255, alpha: 1.0)
    }
    
    class func ebonyClay() -> UIColor {
        
        return UIColor(red: 34/255, green: 49/255, blue: 199/255, alpha: 1.0)
    }
    
    
}
